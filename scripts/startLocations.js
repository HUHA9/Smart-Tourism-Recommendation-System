  // ===== UI helper =====
  window.initStartLocationSelector = function (city) {
    const typeSel = document.getElementById('startType');
    const locSel = document.getElementById('startLocation');
  
    if (!typeSel || !locSel) return;
  
    function refresh() {
      const type = typeSel.value;
      const list = window.startLocations?.[city]?.[type] || [];
  
      locSel.innerHTML = '';
      list.forEach(p => {
        const opt = document.createElement('option');
        opt.value = p.id;
        opt.textContent = p.name;
        opt.dataset.lat = p.lat;
        opt.dataset.lng = p.lng;
        locSel.appendChild(opt);
      });
    }
  
    typeSel.addEventListener('change', refresh);
    refresh();
  };
  